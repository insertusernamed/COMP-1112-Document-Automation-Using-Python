# For Loop
print("For Loop Example:")
for i in range(5):
    print("i =", i)

# While Loop
print("While Loop Example:")
count = 0
while count < 5:
    print("count =", count)
    count += 1

# Nested Loops
print("Nested Loop Example:")
for i in range(3):
    for j in range(2):
        print("i =", i, ", j =", j)
