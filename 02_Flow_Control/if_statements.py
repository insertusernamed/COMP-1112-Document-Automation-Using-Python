# Basic If-Else
x = 10
if x > 5:
    print("x is greater than 5")
else:
    print("x is less than or equal to 5")

# Nested If-Else
y = 20
if y > 15:
    if y > 25:
        print("y is greater than 25")
    else:
        print("y is between 15 and 25")
else:
    print("y is 15 or less")

# Elif Ladder
z = 30
if z == 10:
    print("z is 10")
elif z == 20:
    print("z is 20")
elif z == 30:
    print("z is 30")
else:
    print("z is something else")
